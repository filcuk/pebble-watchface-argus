void quiet_mode_icon_draw(GContext *ctx, int x, int y) {
  for (int row = 0; row < QUIET_MODE_ICON_HEIGHT; row++) {
    for (int col = 0; col < QUIET_MODE_ICON_WIDTH; col++) {
      uint32_t pixel = s_quiet_mode_icon_data[row * QUIET_MODE_ICON_WIDTH + col];
      if (pixel == 0) {
        continue;
      }

      if (pixel != 0xffffffff) {
        graphics_context_set_fill_color(ctx, GColorRed);
      } else {
        graphics_context_set_fill_color(ctx, GColorWhite);
      }

      graphics_fill_rect(ctx, GRect(x + col, y + row, 1, 1), 0, GCornerNone);
    }
  }
}
