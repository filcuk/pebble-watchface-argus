static void prv_draw_icon(GContext *ctx, int x, int y, const uint32_t *data, bool use_red) {
  for (int row = 0; row < BT_ICON_HEIGHT; row++) {
    for (int col = 0; col < BT_ICON_WIDTH; col++) {
      uint32_t pixel = data[row * BT_ICON_WIDTH + col];
      if (pixel == 0) {
        continue;
      }

      if (use_red && pixel != 0xffffffff) {
        graphics_context_set_fill_color(ctx, GColorRed);
      } else {
        graphics_context_set_fill_color(ctx, GColorWhite);
      }

      graphics_fill_rect(ctx, GRect(x + col, y + row, 1, 1), 0, GCornerNone);
    }
  }
}

void bt_icon_draw_connected(GContext *ctx, int x, int y) {
  prv_draw_icon(ctx, x, y, s_bt_connected_data, false);
}

void bt_icon_draw_lost(GContext *ctx, int x, int y) {
  prv_draw_icon(ctx, x, y, s_bt_lost_data, true);
}
